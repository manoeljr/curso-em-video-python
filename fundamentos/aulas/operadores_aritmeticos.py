"""
Operadores Aritimeticos

    - Adição, somar(+)
    - Subtração, diminuir(-)
    - Multiplicação, multiplicar(*)
    - Divisão, dividir(/)
    - Potência, pontenciação(**)
    - Divisão Inteira, parte inteira da divisão(//)
    - Resto da divisão, resto(%)
"""

print(f'Soma 2 + 2 = {2 + 2}')
print(f'Subtração 2 - 2 = {2 - 2}')
print(f'Multiplicação 2 * 2 = {2 * 2}')
print(f'Divisão 2 / 2 = {2 / 2}')
print(f'Potência 2 ** 2 = {2 ** 2}')
print(f'Divisão Inteira 2 // 2 = {2 // 2} só retorna a parte inteira da divisão')
print(f'Divisão com resto 2 % 2 = {2 % 2} só retorna o resto da divisão')


